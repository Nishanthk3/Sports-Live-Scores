package com.nishanth.sportsscore.api;

public class News {
	
	public String b;
	public String id;
	public String s;
	public String t;
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public String getT() {
		return t;
	}
	public void setT(String t) {
		this.t = t;
	}
	
}
